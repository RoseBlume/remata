pub type Tag = (u16, &'static str, Option<&'static str>);

pub fn tag_info(tag: u16) -> (&'static str, Option<&'static str>) {
    TAGS
        .iter()
        .find(|(t, _, _)| *t == tag)
        .map(|(_, name, desc)| (*name, *desc))
        .unwrap_or(("Unknown", None))
}

